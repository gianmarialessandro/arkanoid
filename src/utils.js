export function fixPaginettaPageWidth() {
    document.body.classList.add('paginetta');
}
export function clearPaginettaPageWidth() {
    document.body.classList.remove('paginetta');
}
export function fixHomePageWidth() {
    document.body.classList.add('home');
}
export function clearHomePageWidth() {
    document.body.classList.remove('home');
}